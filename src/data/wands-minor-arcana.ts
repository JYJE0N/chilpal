// 유틸리티 함수들
export const getWandsCardById = (id: number): TarotCard | undefined => {
  return wandsCards.find((card) => card.id === id);
};

export const getWandsCardByNumber = (
  number: number | string
): TarotCard | undefined => {
  return wandsCards.find((card) => card.number === number);
};

export const shuffleWandsCards = (): TarotCard[] => {
  return [...wandsCards].sort(() => Math.random() - 0.5);
};

