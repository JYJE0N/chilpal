// 유틸리티 함수들
export const getPentaclesCardById = (id: number): TarotCard | undefined => {
  return pentaclesCards.find((card) => card.id === id);
};

export const getPentaclesCardByNumber = (
  number: number | string
): TarotCard | undefined => {
  return pentaclesCards.find((card) => card.number === number);
};

export const shufflePentaclesCards = (): TarotCard[] => {
  return [...pentaclesCards].sort(() => Math.random() - 0.5);
};
