// 유틸리티 함수들
export const getSwordsCardById = (id: number): TarotCard | undefined => {
  return swordsCards.find((card) => card.id === id);
};

export const getSwordsCardByNumber = (
  number: number | string
): TarotCard | undefined => {
  return swordsCards.find((card) => card.number === number);
};

export const shuffleSwordsCards = (): TarotCard[] => {
  return [...swordsCards].sort(() => Math.random() - 0.5);
};
